int main()
{
	u8 *buf = NULL, *newExeFSbuf = NULL, *newRomFSbuf = NULL;
	FILE *f = fopen("content.bin","rb");
	if(!f)
	{
		puts("Could not open content.bin!");
		goto end;
	}
	fseek(f,0,SEEK_END);
	size_t fsize = ftell(f);
	fseek(f,0,SEEK_SET);
	buf = malloc(fsize);
	if(!buf)
	{
		printf("Unable to allocate %i bytes for content!\n",fsize);
		goto end;
	}
	fread(buf,fsize,1,f);
	fclose(f);
	if(memcmp(buf+0x100,"NCCH",4) != 0)
	{
		puts("Content is not a NCCH!");
		goto end;
	}
	else if((*(buf+0x18F) & 4) == 0)
	{
		puts("Content is encrypted, please decrypt it with Decrypt9WIP!");
		goto end;
	}
	f = fopen("exefs.bin","rb");
	if(!f)
	{
		puts("Could not open exefs.bin!");
		goto end;
	}
	fseek(f,0,SEEK_END);
	size_t newExeFSsize = ftell(f);
	fseek(f,0,SEEK_SET);
	newExeFSbuf = malloc(newExeFSsize);
	if(!newExeFSbuf)
	{
		printf("Unable to allocate %i bytes for exefs!\n",newExeFSsize);
		goto end;
	}
	fread(newExeFSbuf,newExeFSsize,1,f);
	fclose(f);
	size_t exefspos = *(u32*)(buf+0x1A0)*0x200;
	//ExeFS size
	size_t oldexefssize = *(u32*)(buf+0x1A4);
	*(u32*)(buf+0x1A4) = newExeFSsize / 0x200;
	printf("Old ExeFS Size: %x, New ExeFS Size: %x\n",oldexefssize*0x200,*(u32*)(buf+0x1A4)*0x200);
	size_t exefsHashSize = *(u32*)(buf+0x1A8)*0x200;
	sha2(newExeFSbuf,exefsHashSize,buf+0x1C0,0);
	puts("Recalculated SHA2 for ExeFS");
	size_t romfssize = *(u32*)(buf+0x1B4)*0x200;
	if(romfssize)
	{
		size_t oldromfspos = *(u32*)(buf+0x1B0)*0x200;
		newRomFSbuf = malloc(romfssize);
		if(!newRomFSbuf)
		{
			printf("Unable to allocate %i bytes for romfs!\n",romfssize);
			goto end;
		}
		memcpy(newRomFSbuf, buf+oldromfspos, romfssize);
		//RomFS offset
		*(u32*)(buf+0x1B0) = *(u32*)(buf+0x1A0)+*(u32*)(buf+0x1A4);
		printf("Old RomFS Offset: %x, New RomFS Offset: %x\n",oldromfspos,*(u32*)(buf+0x1B0)*0x200);
	}
	//NCCH size
	size_t oldncchsize = *(u32*)(buf+0x104);
	*(u32*)(buf+0x104) = (exefspos+newExeFSsize+romfssize) / 0x200;
	printf("Old NCCH Size: %x, New NCCH Size: %x\n",oldncchsize*0x200,*(u32*)(buf+0x104)*0x200);
	printf("Signing Content, RsaSignVerify=%i\n",
		RsaSignVerify(buf+0x100,0x100,buf,mod,priv_exp,RSA_2048_SHA256,CTR_RSA_SIGN));
	f = fopen("content.bin","wb");
	if(!f)
	{
		puts("Could not write to content.bin!");
		goto end;
	}
	fwrite(buf,exefspos,1,f);
	fwrite(newExeFSbuf,newExeFSsize,1,f);
	if(newRomFSbuf) fwrite(newRomFSbuf,romfssize,1,f);
	puts("Wrote content.bin");
end:
	if(f) fclose(f);
	if(buf) free(buf);
	if(newExeFSbuf) free(newExeFSbuf);
	if(newRomFSbuf) free(newRomFSbuf);
	return 0;
}
